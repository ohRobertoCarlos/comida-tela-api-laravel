    {
        //
    }

    /**
     * Handle the User "updated" event.
     */
    public function updated(User $user): void
    {
        if ($user->wasChanged('email')) {
            $user->email_verified_at = null;
            $user->saveQuietly();
            $user->sendEmailVerificationNotification();
        }
    }

    /**
     * Handle the User "deleted" event.
     */
